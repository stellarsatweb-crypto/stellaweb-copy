-- =========================
-- 6. VERIFY DATA
-- =========================
-- View all regions with sites
SELECT r.region_name, s."SITENAME"
FROM regions r
LEFT JOIN site_inventory s
ON r.region_name = s.region_name
ORDER BY r.region_name;

-- Count sites per region
SELECT region_name, COUNT(*) AS total_sites
FROM site_inventory
GROUP BY region_name
ORDER BY region_name;