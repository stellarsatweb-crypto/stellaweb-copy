-- =========================
-- 2. CREATE REGIONS TABLE
-- =========================
CREATE TABLE regions (
    id SERIAL PRIMARY KEY,
    lot_number INTEGER UNIQUE NOT NULL,
    region_name VARCHAR(100) UNIQUE NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

