-- =========================
-- 3. CREATE SITE INVENTORY
-- =========================
CREATE TABLE site_inventory (
    id SERIAL PRIMARY KEY,
    region_name VARCHAR(100) REFERENCES regions(region_name) ON DELETE CASCADE,
    "NO." TEXT,
    "SITENAME" TEXT,
    "PROVINCE" TEXT,
    "MUNICIPALITY" TEXT,
    "REGION" TEXT,  -- after MUNICIPALITY
    "PHASE 1 ORIGINAL AIRMAC" TEXT,
    "SPARE MODEM USED" TEXT,
    "SPARE MODEM USED " TEXT, -- only PANGASINAN
    "PHASE 2 AIRMAC" TEXT,
    "PHASE 1 GRAFANA AIMAC" TEXT,
    "PHASE 1 IP ADDRESS" TEXT,
    "PHASE 2 IP ADDRESS" TEXT,
    "PHASE 1 SERIAL" TEXT,
    "PHASE 1 TRANCEIVER" TEXT,
    "PHASE 1 EAP225-outdoor s/n 1" TEXT,
    "PHASE 1 EAP225-outdoor s/n 2" TEXT,
    "PHASE 1 ER-605 SERIAL" TEXT,
    "PHASE 1 LAT" TEXT,
    "PHASE 1 LONG" TEXT,
    "PHASE 1 TRANCEIVER MODEL" TEXT,
    "PHASE 1 MODEM MODEL" TEXT,
    "PHASE 2 EAP225-outdoor s/n 1" TEXT,
    "PHASE 2 ASSIGNED KAD" TEXT,
    "INSTALLATION DATE" TEXT,
    "UAT SPEEDTEST MIR DL" TEXT,
    "UAT SPEEDTEST MIR UL" TEXT,
    "LATENCY" TEXT,
    "UAT SPEEDTEST CIR AP1 DL" TEXT,
    "UAT SPEEDTEST CIR AP1 UL" TEXT,
    "AP1 LATENCY" TEXT,
    "UAT SPEEDTEST CIR AP2 DL" TEXT,
    "UAT SPEEDTEST CIR AP2 UL" TEXT,
    "AP2 LATENCY" TEXT,
    "UAT TEST DATE (DICT INPUT)" TEXT,
    "LAT" TEXT,
    "LONG" TEXT,
    "2 - 3 SITE CONTACTS" TEXT,
    "OLD EAP225" TEXT,
    "SITE CONTACT EMAIL/SOCMED" TEXT,
    "REMARKS" TEXT
);

-- =========================
-- 4. CREATE INDEX
-- =========================
CREATE INDEX idx_inventory_region ON site_inventory(region_name);

